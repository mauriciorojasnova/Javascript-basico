//Existen valores que pueden ser True o False

Boolean(0); //False

Boolean(null); //False

Boolean(undefined); //False

Boolean(NaN); //False

Boolean(""); //Flase

Boolean(1); //True para cualquier número igual o mayor a 1

Boolean("a") //True para cualquier string

Boolean([]); //True para cualquier array

Boolean({}); //True para cualquier objeto

Boolean(function(){}); //True para cualquier función